package velocity;

public class Staticmethods {
	public static void ashish() {
		for(int i =1;i<=5;i++) {
			for(int k=1;k<=i;k++) {
				System.out.print(" *");
			}System.out.println();
		}
	}

public static void dhiraj() {
	for(int i =4;i>=1;i--) {
		for(int k=1;k<=i;k++) {
			System.out.print(" *");
		}System.out.println();
	}
}


public static void main(String[] args) {
ashish();
dhiraj();

}
}

